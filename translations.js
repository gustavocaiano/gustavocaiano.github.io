// Current language state
let currentLanguage = 'en';

// Function to get browser language
function getBrowserLanguage() {
    const lang = navigator.language || navigator.userLanguage;
    return lang.startsWith('pt') ? 'pt' : 'en';
}

// Function to set language
function setLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('preferred-language', lang);
    updatePageContent();
    updateLanguageSelector();
}

// Function to update page content
function updatePageContent() {
    const t = translations[currentLanguage];
    
    // Update title
    document.title = t.title;
    
    // Update all elements with data-translate attribute
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (t[key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = t[key];
            } else {
                element.textContent = t[key];
            }
        }
    });

    // Update subject datalist options
    const datalist = document.getElementById('subjects');
    if (datalist) {
        const subjects = [
            t.contactSubjectLaravelProduct || t.personalProject,
            t.contactSubjectLaravelConsulting || t.softwareConsulting,
            t.contactSubjectLaravelRole || t.jobOpportunity,
            t.contactSubjectPartnership || t.generalInquiry
        ];
        
        datalist.innerHTML = '';
        subjects.forEach(subject => {
            if (subject) {
                const option = document.createElement('option');
                option.value = subject;
                datalist.appendChild(option);
            }
        });
    }
}

// Function to update language selector
function updateLanguageSelector() {
    const selector = document.getElementById('languageSelector');
    if (selector) {
        selector.value = currentLanguage;
    }
}

// Initialize language system
function initializeLanguage() {
    // Get saved language or browser language
    const savedLanguage = localStorage.getItem('preferred-language');
    const browserLanguage = getBrowserLanguage();
    currentLanguage = savedLanguage || browserLanguage;
    
    // Update content
    updatePageContent();
    updateLanguageSelector();
}

// Export for use in other scripts
if (typeof module !== 'undefined' && module.exports) {
    module.exports = { translations, setLanguage, initializeLanguage };
} 
